class StringSwitch {
	public static void main(String args[]) {
		int i=2; 
		switch(i)
		{
			case 1:
				System.out.println(" ONE "); 
				break;

			case 2:
				System.out.println(" TWO "); 
				break;

			case 3:
				System.out.println(" THREE "); 
				break;

			default:
				System.out.println(" NO MATCH "); 
				break;
		}
	}
}	